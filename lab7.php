function getDayOfWeek($dayNumber) {
    $days = array(
        1 => "Понедельник",
        2 => "Вторник",
        3 => "Среда",
        4 => "Четверг",
        5 => "Пятница",
        6 => "Суббота",
        7 => "Воскресенье"
    );
    
    if ($dayNumber >= 1 && $dayNumber <= 7) {
        return $days[$dayNumber];
    } else {
        return "Некорректный номер дня";
    }
}

